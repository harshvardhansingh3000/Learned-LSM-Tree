#pragma once

#include <cstdint>
#include <string>

namespace lsm {

// в”Җв”Җв”Җ SSTable File Format в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
//
// An SSTable (Sorted String Table) is an IMMUTABLE file on disk containing
// sorted key-value pairs. Once written, it is never modified вҖ” only read or deleted.
//
// File Layout (updated with Bloom filter):
// в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
// в”Ӯ                    Data Block 0                          в”Ӯ
// в”Ӯ  в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ           в”Ӯ
// в”Ӯ  в”Ӯkeylenв”Ӯkey   в”Ӯtype в”Ӯseqnum в”Ӯvlen в”Ӯvalue  в”Ӯ (entry 1) в”Ӯ
// в”Ӯ  в”Ӯkeylenв”Ӯkey   в”Ӯtype в”Ӯseqnum в”Ӯvlen в”Ӯvalue  в”Ӯ (entry 2) в”Ӯ
// в”Ӯ  в””в”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ           в”Ӯ
// в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
// в”Ӯ                    Data Block N                          в”Ӯ
// в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
// в”Ӯ                    Index Block                           в”Ӯ
// в”Ӯ  For each data block:                                   в”Ӯ
// в”Ӯ  [first_key (string)] [block_offset (8)] [block_size(4)]в”Ӯ
// в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
// в”Ӯ                    Bloom Filter Block                    в”Ӯ
// в”Ӯ  [num_hash_functions (4)] [data_size (4)] [data...]     в”Ӯ
// в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
// в”Ӯ                    Footer                                в”Ӯ
// в”Ӯ  [index_offset(8)] [index_size(4)] [entry_count(4)]     в”Ӯ
// в”Ӯ  [min_key_len(4)] [min_key] [max_key_len(4)] [max_key]  в”Ӯ
// в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
// в”Ӯ  [bloom_offset (4 bytes)]  вҶҗ offset where bloom starts  в”Ӯ
// в”Ӯ  [magic_number (4 bytes)]  вҶҗ always 0x53535401           в”Ӯ
// в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
//
// Reading strategy:
//   1. Read last 8 bytes: bloom_offset(4) + magic(4)
//   2. Validate magic number
//   3. Read bloom filter from bloom_offset
//   4. Parse footer (between index block end and bloom_offset)
//   5. Read index block from index_offset
//   6. Point lookups: bloom filter вҶ’ fence pointers вҶ’ data block

// Magic number to identify SSTable files
constexpr uint32_t SSTABLE_MAGIC = 0x53535401;  // "SST\x01"

// в”Җв”Җв”Җ Index Entry в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
struct IndexEntry {
    std::string first_key;   // First key in this data block (fence pointer)
    uint64_t block_offset;   // Byte offset of this data block in the file
    uint32_t block_size;     // Size of this data block in bytes
};

// в”Җв”Җв”Җ Footer в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
struct Footer {
    uint64_t index_offset;   // Where the index block starts
    uint32_t index_size;     // Size of the index block in bytes
    uint32_t entry_count;    // Total number of key-value entries
    std::string min_key;     // Smallest key in this SSTable
    std::string max_key;     // Largest key in this SSTable
    uint32_t bloom_offset;   // Where the bloom filter block starts (read from end of file)
    uint32_t magic;          // Magic number for validation
};

} // namespace lsm
